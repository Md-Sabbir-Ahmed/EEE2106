#include<iostream>
using namespace std;
int main()
{

    int m,n;
    cout<<"Enter two integers (use SPACE to separate): ";
    cin>>m>>n;
    cout<<"m = "<<m<<", n = "<<n<<endl;
    double x,y,z;
    cout<<"Enter three decimal numbers (use SPACE to separate): ";
    cin>>x>>y>>z;
    cout<<"x = "<<x<<", y = "<<y<<", z = "<<z<<endl;
    char c1,c2,c3,c4;
    cout<<"Enter four characters (use SPACE to separate): ";
    cin>>c1>>c2>>c3>>c4;
    cout<<"c1 = "<<c1<<", c2 = "<<c2<<", c3 = "<<c3;
    cout<<", c4 = "<<c4<<endl;
    return 0 ;
}


